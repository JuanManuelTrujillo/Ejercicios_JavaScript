//Impresion de sumas:
var a =Number(prompt("Ingresa Primer Numero para Sumar"));
var b =Number(prompt("Ingresa Segundo Numero para Sumar"));
var z=a+b;
document.write("El resultado de la suma es:"+z+"<br>"); //Imprime suma en documento
console.log("El resultado de la suma es:"+z); //Imprime suma en consola
alert("El resultado de la suma es:"+z); //Imprime la suma como una alerta

